    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(checkTrungMaPT()==true){
            JOptionPane.showMessageDialog(null, "Mã phiếu thuê này đã được lập!!!");
        }
        else{
            String ngaylap=txfNgayLap.getText();
            String mapt=txfMaPT.getText();
            String gia=txfGia.getText();
            if(Controller.themHoaDon(mapt, ngaylap, gia)){
                    JOptionPane.showMessageDialog(null, "thêm hóa đơn thành công");
                    loadData();
                    tayText();
            }
            else
            JOptionPane.showMessageDialog(null, "Thêm hóa đơn thất bại");
        }
    }//GEN-LAST:event_btnThemActionPerformed

