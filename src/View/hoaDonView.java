    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int selectedIndex =HDtable.getSelectedRow();
        if(selectedIndex==-1)
            JOptionPane.showMessageDialog(rootPane,  "Chọn hóa đơn cần xóa");
        int data=HDtable.getSelectedRow();
        String mahd=HDtable.getModel().getValueAt(data, 0).toString();
            int result=JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa không");
            if(result==0){
                if(Controller.xoaHoaDon(mahd)){
                    JOptionPane.showMessageDialog(null, "Xóa thành công");
                    loadTable();
                    tayText();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Xóa thất bại");
                     tayText();
                }
            
            }
    }//GEN-LAST:event_btnXoaActionPerformed

